#!/bin/bash

# Простой скрипт для отображения текста
# Кросс-платформенный (работает в Termux, Linux, macOS)

# Проверяем, передан ли аргумент
if [ $# -eq 0 ]; then
    echo "Использование: $0 \"Ваш текст\""
    echo "Пример: $0 \"Привет, мир!\""
    exit 1
fi

# Отображаем переданный текст
echo "Вывод текста:"
echo "-------------"
echo "$1"
echo "-------------"

# Дополнительная информация
echo ""
echo "Скрипт выполнен успешно!"
echo "Платформа: $(uname -o 2>/dev/null || uname -s)"
